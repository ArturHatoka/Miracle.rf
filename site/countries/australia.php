            <b>Общие данные</b>
            <p>Даже истории о птицах-убийцах, утках-демонах и метровых кузнечиках не могут спугнуть желающих отправиться в Австралию: эта страна не перестает привлекать путешественников своим географическим, природным и экскурсионным разнообразием. Так что можно со спокойной совестью заявить, что австралийский турист — он особый: ни капли не боится обгореть на палящем солнце, готов больше суток добираться до континента на перекладных и не смущается наличием всевозможных фантастических гадов, от метровых кузнечиков до сумчатых дьяволов.
            </p>
            <p>За такую твердость духа Австралия вознаграждает сторицей: ажурными архипелагами Большого Барьерного Рифа, кроваво-красным монолитом Улуру, открыточными волнами для серферов и плюшевыми ручными коалами для сентиментальных барышень. Прибавим к этому великолепные пляжи и узнаваемые силуэты достопримечательностей типа Сиднейской оперы и моста Харбор, кучу интересных вещичек, которые можно увезти с собой и легкость общения с местным населением: английский язык наши туристы худо-бедно выучили. «Осси, ай’м каминг!» — воскликнули мы и основательно проштудировали для вас южный континент.
            </p>
