            <b>Столица, крупные города</b>
            <p>Столица — Веллингтон (Wellington).
            </p>
            <p>Крупные города: Окленд (Auckland), Квинстаун (Queenstown), Крайстчерч (Christchurch), Данидин (Dunedin).
            </p>
            <b>Разница с Москвой во времени</b>
            <p>Опережает на 9 часов зимой и на 8 летом
            </p>
            <b>Телефонный код</b>
            <p>64
            </p>
            <b>Денежная единица</b>
            <p>Новозеландский доллар (NZD), 1 NZD = 100 центов, 1 USD ≅ 1,29 NZD, 1 EUR ≅ 1,77 NZD
            </p>
            <b>Стоимость типового ужина</b>
            <p>30-40 NZD
            </p>
            <b>Чаевые</b>
            <p>~10%
            </p>
            <b>Государственный язык</b>
            <p>Английский и маори
            </p>
            <b>Население</b>
            <p>4,11 млн. человек
            </p>
            <b>Общие данные</b>
            <p>Новая Зеландия славится своей шикарной природой — гейзерами, горами и озёрами, лесами и гротами, ледниками и пляжами. Широко развитое экологическое движение позволяет сохранить всё это великолепие в почти первозданном состоянии, даже в районах крупных городов. Кроме красивейшей уникальной природы и насыщенной «экскурсионки», страна имеет ещё один очень весомый плюс в глазах определённой категории туристов — заслуженное звание родины экстремального и активного туризма. Сюда едут туристы, которые много где побывали и хотят увидеть что-то особенное. Отдельная группы путешественников — «экстремальщики». Уровень дохода — выше среднего: перелёт дорогой, а туры в страну часто совмещают с посещением Австралии; всё это недёшево.
            </p>
            <b>Как добраться до Новой Зеландии</b>
            <p>Из Москвы в Окленд можно добраться несколькими способами:
            </p>
            <p>Рейсами компаний «Аэрофлот» и Cathay Pacific Москва — Гонконг — Окленд (общее время перелёта, включая ожидание стыковки — около 26 часов). Это самый удобный вариант.
            </p>
            <p>Рейсами а/к JAL до Токио, далее с Air New Zealand (общее время в полёте — около 32 часов).
            </p>
            <p>Рейсами Korean Air Москва — Сеул — Окленд (общее время в полёте 30-35 часов, в зависимости от стыковки).
            </p>
            <p>Рейсами а/к Emirates Москва — Дубай — Окленд (в полёте — около 30 часов). Минус этого способа — целых три посадки (кроме Дубая и Окленда, всегда есть посадка в одном из городов Австралии, как правило, Сиднее или Мельбурне).
            </p>
            <p>Рейсами «Аэрофлота» и American Airlines (а также Qantas Airways, United Airways, Air New Zealand) Москва — Лос-Анджелес — Окленд (около 30,5 часов). Минус такого варианта — надо ставить американскую визу.
            </p>
            <p>Рейсами компаний Air China, Dragonair, Air New Zealand Москва — Пекин — Гонконг — Окленд (около 30 часов).
            </p>
            <p>Также есть варианты рейсов через Сидней, Дели, Париж, Сингапур и прочие. Поскольку большинство туристов совмещает визит в Новую Зеландию с посещением Австралии, то и перелёты в основном делаются через Австралию.
            </p>
            <b>Виза в Новую Зеландию</b>
            <p>Для посещения Новой Зеландии гражданам России необходима виза.
            </p>
            <b>Таможня</b>
            <p>Ввоз и вывоз иностранной и национальной валюты неограничен (декларация обязательна, если сумма превышает 10 000 NZD). Граждане старше 17 лет могут беспошлинно ввезти до 200 сигарет или 50 сигар или 250 гр табака или смесь всех трёх видов — не больше, чем 250 гр, до 4,5 л вина или пива, до 1,125 л крепких алкогольных напитков или чистого спирта, а также товары личного употребления суммарной стоимостью до 700 NZD.
            </p>
            <p>Запрещён ввоз продуктов питания (в том числе большинства видов консервов), наркотиков, любых растений, предметов животного и растительного происхождения (включая почву и древесину), продуктов пчеловодства, кормов для домашних животных, слоновой кости в любой форме, изделий из панциря черепахи, кости и бивней морского зверя, рогов носорога и шкуры тигра, а также изделий из кожи представителей семейства кошачьих. Запрещён ввоз целого ряда мочегонных, снотворных и успокоительных средств, сердечных препаратов, транквилизаторов, всех видов вакцин, а также лекарств, для изготовления которых используется мускус (если их необходимость не подтверждается соответствующими врачебными предписаниями). Ввоз оружия запрещён, но если заранее было получено специальное разрешение новозеландской полиции, можно ввозить огнестрельное и холодное оружие с обязательным таможенным декларированием. Ввоз домашних животных из стран, где зарегистрирован вирус бешенства или недостаточен контроль над этим заболеванием (а Россия в такую категорию включена) не допускается ни в каком виде.
            </p>
