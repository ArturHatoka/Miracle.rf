            </p>
            <b>Транспорт Новой Зеландии</b>
            <p>В стране хорошо развиты междугородные автобусные маршруты, правда, билеты на такие рейсы недёшевы (поездка из Веллингтона в Окленд — около 100 NZD). Действует система скидок и льготных проездных. На автобусы небольших местных компаний цены несколько ниже. Билеты на любой междугородний автобус надо бронировать заранее.
            </p>
            <p>Городские автобусы есть в Окленде, Веллингтоне, Крайстчерче и Данидине. В Веллингтоне также ходят троллейбусы. Стоимость билета — от 1,5 до 2,5 NZD в зависимости от продолжительности поездки (маршруты поделены на зоны). Билеты можно приобрести в специальных киосках. Между Северным и Южным островами ходят паромы, по нескольку рейсов в день из Веллингтона в Пиктон. Время в пути — 3 часа, стоимость поездки ~44 NZD (или ~22 NZD, если билет заказан за неделю). Также между островами ходят быстроходные пассажирские катамараны (время в пути — 2 часа 15 минут), но билеты на них стоят дороже — около 60 NZD. Эти билеты тоже лучше бронировать заранее.
            </p>
            <p>Такси в Новой Зеландии распространены повсеместно. Стоимость поездки — около 1 NZD с человека за посадку и 4-5 NZD за каждый километр. Железнодорожный транспорт в стране развит сравнительно слабо (всего 8 линий), зато плата за проезд поездом часто меньше, чем за автобус, следующий по тому же маршруту.
            </p>
            <b>Аренда авто</b>
            <p>Чтобы взять авто напрокат, необходимо предъявить действующие права международного образца на английском языке и быть не моложе 21 года. Стоимость аренды — от 60 NZD в сутки (страховка включена), при длительной аренде (от трёх недель) арендная плата снижается почти вдвое.
            </p>
            <b>Безопасность туристов в Новой Зеландии</b>
            <p>Новая Зеландия — очень безопасная страна, отношение к туристам здесь весьма доброжелательное. Уровень преступности в стране очень низкий. Здесь нет ядовитых змей или опасных животных, почти отсутствуют кровососущие насекомые. Единственные вредные насекомые — песчаные блохи, которых немало на пляже (они, как и обычные среднерусские комары, боятся репеллентов). Также на островах водится очень редкий ядовитый паук «катило».
            </p>
            <p>Вода из-под крана годится для питья. Молоко и молокопродукты проходят тщательную обработку. Мясо, домашняя птица, морепродукты, овощи и фрукты — всё это тоже вполне безопасно, если не забывать об элементарных правилах гигиены.
            </p>
            <p>Табак продаётся повсеместно, но стоит очень дорого. Курение запрещено во всех общественных местах, во всех видах транспорта, а также в отелях и даже некоторых ресторанах. Крепкие спиртные напитки продаются только в специализированных магазинах «Bottle Store». В некоторые рестораны можно приносить собственные спиртные напитки: на дверях таких заведениях висит табличка с аббревиатурой «BYO» (Bring Your Own).
            </p>
            <p>На улицах и в общественных заведениях очень чисто, сорить на улицах не принято.
            </p>
            <p>Местное население приветливо и дружелюбно. Особых ограничений на одежду нет, даже при посещении церквей и традиционных праздников маори. Ограничений на фотографирование тоже нет, но всё же в музеях и церквях следует спрашивать разрешение (или обращать внимание на поясняющие таблички).
            </p>
            <b>Климат Новой Зеландии</b>
            <p>В стране субтропический морской климат. Январь — самый тёплый месяц (разгар местного лета), средняя температура на Северном Острове +19 °C, на Южном +14 °C. Июль — самый холодный месяц, средняя температура на Северном Острове +12 °C, на Южном +5 °C. Новозеландские зимы сопровождаются обильными снегопадами на территории местных Альп (Южный Остров) и в горах в центре Северного Острова. Иногда снега выпадают и в низменных районах страны, главным образом на юге Южного Острова, но чаще там идут сильные дожди. Приливы утром и отливы вечером весьма ощутимы.
            </p>
            <b>Отели Новой Зеландии</b>
            <p>Отели хорошего уровня, их «звёздность» вполне соответствует международным стандартам. Есть «лоджи» — отели, расположенные на природе (обычно рядом с источником воды).
            </p>
            <p>Напряжение в сети 220 В, 50 Гц. Розетки трёхконтактные с плоскими штырьками, переходник можно купить или взять напрокат в отеле. Большинство гостиниц подаёт в номера напряжение в 110 В мощностью не более 20 Вт на розетку, достаточное только для электрических бритв.
            </p>
            <b>Деньги</b>
            <p>Валюту можно обменять в аэропортах, отделениях банков и специализированных обменных пунктах. Банки работают с 9:00 до 16:30 с понедельника по пятницу.
            </p>
            <p>Кредитные карты ведущих мировых систем и дорожные чеки обслуживаются повсеместно. Туристические чеки обмениваются по официальному курсу в банках, крупных гостиницах и в некоторых магазинах. Чтобы избежать дополнительных расходов при обмене, лучше использовать туристические чеки в долларах США, фунтах стерлингов или австралийских долларах.
            </p>
            <p>В новозеландском ресторане (но не в простом кафе) принято оставлять 10 % от суммы заказа. В провинциальных заведениях чаевые обычно не берут. Также принято оставлять «на чай» гидам и водителям.
            </p>
            <b>Шоппинг и магазины</b>
            <p>Магазины обычно открыты в будни с 9:00 до 17:30, многие крупные магазины работают и по выходным — с 10:00 до 13:00. В курортных районах большинство магазинов открыто с 9:00 до 19:00-21:00.
            </p>
            <p>В стране можно прикупить одежду хорошего качества, детские товары, изделия из шкур овец. Местные сувениры — образцы народного творчества Маори, украшения из перламутра новозеландских устриц, лечебная косметика на основе уникальных растений, произрастающих только в Новой Зеландии.
            </p>
            <b>Кухня и рестораны Новой Зеландии</b>
            <p>На островах «рулит» англосаксонская кухня с её традиционными рыбой или мясом с жареной картошкой, бифштексами и ростбифами. Как ни странно, морепродукты сравнительно редки, но в меню всё же можно найти устриц, лангустов, ракообразных и разные сорта рыбы. Местная кулинарная «фишка» — жареный или запечённый сладкий картофель «кумара» и различные продукты, приготовленные на открытом огне.
            </p>
            <p>В Новой Зеландии производят множество первоклассных сыров «французского типа». Из спиртных напитков заслуживают внимания местные пиво и вино, которые считаются одними из лучших в мире.
            </p>
            <b>Развлечения, экскурсии и достопримечательности Новой Зеландии</b>
            <p>Квинстаун — второй по посещаемости туристический центр страны, он служит отправной точкой для однодневных туров к фьордам Милфорд-Саунд и Даутфул-Саунд. Отсюда же отправляются экскурсии на вертолётах к ледникам, куда невозможно попасть по земле. Другие «must see»: Те-Анау — мир подземных залов и соборов, рек и озёр, гора Кука — высочайшая точка Новой Зеландии и Тасманский ледник.
            </p>
            <p>Из Окленда начинаются многие частные или автобусные туры по стране, также он является портом захода всех международных парусных регат, проходящих в этом регионе, и всех кругосветных гонок.
            </p>
            <p>Роторуа — одна из главных достопримечательностей страны. Здесь расположены знаменитая Долина Гейзеров, культурный центр новозеландских аборигенов маори, термальный парк и одновременно стилизованная «деревня маори» Whakarewarewa Thermal Reserve, где в специальном затемнённом помещении можно увидеть нелетающую птицу киви — символ страны. Buried Village of the Wairoa — погребённая извержением вулкана в 1886 году деревня маори, музей маори, водопады. В Тайпо можно посмотреть городскую ратушу, англиканский собор, ботанический сад, живописную Трансальпийскую дорогу, ледники Франс-Джозев и Фокс.
            </p>
            <p>Другие достопримечательности страны: Каикоура — место на восточном побережье Южного Острова севернее Крайстчерча, где можно наблюдать китов. Оrana Wildlife Park — самый большой зоопарк Новой Зеландии со множеством животных, в том числе — уникальными новозеландскими нелетающими птицами. Hanmer Springs Thermal Reserve — горячие минеральные источники. На территории национального парка Тонгариро (входит в список всемирного наследия ЮНЕСКО) находится три действующих вулкана: Тонгариро, Наурохе и Руапеху, причём на их склонах работают лыжные станции.
            </p>
            <p>Экстрим в Новой Зеландии — понятие, которое давно стало известно всему миру. Новозеландцы буквально помешаны на экстремальных развлечениях, возможностей для которых в стране немало. В Квинстауне можно развлечь себя рыбалкой и особым новозеландским видом аттракционов Luge ride. Luge ride — это 800-метровая бетонная трасса шириной в 4 метра, по которой спускаются с горы на специальных трёхколёсных картах — «люжах». Такое же развлечение есть в Роторуа. Кроме того, для любителей острых ощущений в Роторуа имеются: «банги-джампинг», тобогган (катание с горок на специальных снарядах наподобие мини-бобов), «джет-боут», «зорбинг» (скатывание с горы внутри огромной надувной сферической конструкции, неслабая тренировка вестибулярного аппарата) и воздушные аттракционы Freefall.
            </p>
            <p>Таупо славится рыбалкой на форель в любое время года. Ещё здесь можно совершить круиз по озеру, покататься на скоростной лодке, совершить сплав по горным рекам или полёт на самолёте над горами и действующим вулканом White island.
            </p>
            <b>Дайвинг</b>
            <p>Дайвинг распространён в основном в районе Северного Острова — на островах Пур-Найтс (Poor Knights Islands). А на Южном Острове организуется дайвинг в районе затонувшего в 1986 году в районе национального парка Милфорд-Саунд советского круизного лайнера «Михаил Лермонтов».
                Сезон здесь продолжается круглый год, каждый период имеет свои особенности: с января по апрель температура воды +20..+23°C, видимость 20 м; с мая по сентябрь +15..+17°C, видимость до 30 м, с сентября по январь +20 °C, видимость похуже (зато в это время можно увидеть самое большое количество видов рыб).
            </p>
